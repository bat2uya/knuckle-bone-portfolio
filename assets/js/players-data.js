window.getAllPlayers = function getAllPlayers() {
  const rows = [];
  for (const [regionKey, regionData] of Object.entries(window.playersData || {})) {
    for (const city of (regionData.cities || [])) {
      for (const club of (city.clubs || [])) {
        for (const team of (club.teams || [])) {
          for (const player of (team.players || [])) {
            rows.push({
              ...player,
              region: regionKey,
              city: city.name,
              club: club.name,
              team: team.name
            });
          }
        }
      }
    }
  }
  return rows.sort((a, b) => a.rank - b.rank);
};
